    /** Detecta a chave da rede pela URL, ou null. */
    public static function detect(string $url): ?string
    {
        $url = trim($url);
        if (stripos($url, 'mailto:') === 0) {
            return 'email';
        }
        $host = strtolower((string) parse_url($url, PHP_URL_HOST));
        if ($host === '') {
            return null;
        }
        $host = preg_replace('/^www\./', '', $host) ?? $host;

        foreach (self::HOSTS as $needle => $key) {
            if ($host === $needle || str_ends_with($host, '.' . $needle)) {
                return $key;
            }
        }
        return null;
    }

    /** SVG inline para a URL, ou null se não houver rede reconhecida. */
    public static function svg(?string $url): ?string
    {
        if (!is_string($url) || $url === '') {
            return null;
        }
        $key = self::detect($url);
        if ($key === null || !isset(self::PATHS[$key])) {
            return null;
        }
        return '<svg viewBox="0 0 24 24" fill="currentColor" class="pf-icon shrink-0" aria-hidden="true">'
            . self::PATHS[$key] . '</svg>';
    }
